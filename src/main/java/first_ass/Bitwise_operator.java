package first_ass;

public class Bitwise_operator {
    public static void main(String[] args) {
        int X=170;
        int Y=150;
        System.out.println("X value = "+X+"  Y value = "+Y+"  bitwise AND operator value = "+(X&Y));
        System.out.println("X value = "+X+"  Y value = "+Y+"  bitwise Exclusive  OR operator value = "+(X^Y));
        System.out.println("X value = "+X+"  Y value = "+Y+"  bitwise Inclusive OR operator value = "+(X|Y));
    }
}
