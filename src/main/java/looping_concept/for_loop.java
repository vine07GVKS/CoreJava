import java.util.Scanner;

public class for_loop
{

    public static void main(String[] args)
    {
        Scanner also =new Scanner(System.in);
        System.out.println("enter value");

      for(int now=also.nextInt(); now <=50;now ++)
      {
          System.out.println(" ** ");
      }
    }
}
